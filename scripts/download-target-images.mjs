import { fileURLToPath } from "url";
import fs from "fs";
import path from "path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const targetsDir = path.resolve(root, "public", "targets");
const generatedDir = path.resolve(root, "src", "generated");

const MIME_TO_EXT = {
  "image/png": ".png",
  "image/jpeg": ".jpg",
  "image/jpg": ".jpg",
  "image/svg+xml": ".svg",
  "image/webp": ".webp",
};

function loadSolutions() {
  const battles = JSON.parse(
    fs.readFileSync(path.resolve(root, "data", "battles.json"), "utf8"),
  );

  const dailyDir = path.resolve(root, "data", "daily");
  const daily = [];

  function walk(dir) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        walk(full);
      } else if (entry.isFile() && entry.name.endsWith(".json")) {
        daily.push(...JSON.parse(fs.readFileSync(full, "utf8")));
      }
    }
  }

  if (fs.existsSync(dailyDir)) {
    walk(dailyDir);
  }

  return [...daily, ...battles].filter(
    (s) => s && typeof s.score === "number" && s.score > 0,
  );
}

function extFromUrl(url) {
  try {
    const pathname = new URL(url).pathname;
    const ext = path.extname(pathname);
    if (ext && ext.length <= 5) return ext;
  } catch {
    // ignore malformed URLs
  }
  return "";
}

async function downloadImage(url, filePath) {
  const res = await fetch(url);
  if (!res.ok) {
    throw new Error(`HTTP ${res.status} for ${url}`);
  }
  const buffer = Buffer.from(await res.arrayBuffer());
  fs.writeFileSync(filePath, buffer);
  return res.headers.get("content-type") || "";
}

async function main() {
  fs.mkdirSync(targetsDir, { recursive: true });
  fs.mkdirSync(generatedDir, { recursive: true });

  const solutions = loadSolutions();
  const map = {};

  console.log(`Downloading target images for ${solutions.length} solutions...`);

  for (const solution of solutions) {
    if (!solution.targetImage) continue;

    let ext = extFromUrl(solution.targetImage);
    const fileNameWithoutExt = `${targetsDir}/${solution.id}`;

    try {
      const contentType = await downloadImage(
        solution.targetImage,
        `${fileNameWithoutExt}.tmp`,
      );
      ext = MIME_TO_EXT[contentType] || ext || ".png";
      const finalPath = `${fileNameWithoutExt}${ext}`;
      fs.renameSync(`${fileNameWithoutExt}.tmp`, finalPath);
      map[solution.id] = `/targets/${solution.id}${ext}`;
      console.log(`  ${solution.id} -> /targets/${solution.id}${ext}`);
    } catch (err) {
      if (fs.existsSync(`${fileNameWithoutExt}.tmp`)) {
        fs.unlinkSync(`${fileNameWithoutExt}.tmp`);
      }
      console.warn(`  ${solution.id} failed: ${err.message}`);
    }
  }

  const ts = `// Generated by scripts/download-target-images.mjs — do not edit manually.
export const targetImageMap: Record<string, string> = ${JSON.stringify(map, null, 2)};

export function getTargetImageUrl(id: string, fallback?: string): string {
  return targetImageMap[id] ?? fallback ?? "";
}
`;

  const mjs = `// Generated by scripts/download-target-images.mjs — do not edit manually.
export const targetImageMap = ${JSON.stringify(map, null, 2)};

export function getTargetImageUrl(id, fallback) {
  return targetImageMap[id] ?? fallback ?? "";
}
`;

  fs.writeFileSync(path.join(generatedDir, "target-images.ts"), ts);
  fs.writeFileSync(path.join(generatedDir, "target-images.mjs"), mjs);
  console.log(`Wrote src/generated/target-images.ts with ${Object.keys(map).length} local images`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
